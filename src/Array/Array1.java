package Array;

public class Array1 {
    public static void main(String[] args) {
        int[] students;
        students = new int[5];
        students[0] = 90;
        students[1] = 80;
        students[2] = 70;
        students[3] = 60;
        students[4] = 50;

        System.out.printf("학생1 점수 : %d\n",students[0]);
        System.out.printf("학생2 점수 : %d\n",students[1]);
        System.out.printf("학생3 점수 : %d\n",students[2]);
        System.out.printf("학생4 점수 : %d\n",students[3]);
        System.out.printf("학생5 점수 : %d\n",students[4]);
    }
}
